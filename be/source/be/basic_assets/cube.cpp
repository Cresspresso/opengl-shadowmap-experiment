
#include "be/basic_assets/cube.hpp"

namespace be
{
	namespace basic_assets
	{
		namespace meshes
		{
			be::gl::BasicMesh makeCubeMesh()
			{
				using Vertex = be::gl::BasicVertex;

				float const h = 0.5f;
				Vertex const vertices[4 * 6] = { Vertex
					// back
				{ { -h, -h, +h }, { 0, 0, 1 }, { 0, 0 } },
				{ { +h, -h, +h }, { 0, 0, 1 }, { 1, 0 } },
				{ { +h, +h, +h }, { 0, 0, 1 }, { 1, 1 } },
				{ { -h, +h, +h }, { 0, 0, 1 }, { 0, 1 } },
					// right
				{ { +h, -h, +h }, { 1, 0, 0 }, { 0, 0 } },
				{ { +h, -h, -h }, { 1, 0, 0 }, { 1, 0 } },
				{ { +h, +h, -h }, { 1, 0, 0 }, { 1, 1 } },
				{ { +h, +h, +h }, { 1, 0, 0 }, { 0, 1 } },
					// front
				{ { +h, -h, -h }, { 0, 0, -1 }, { 0, 0 } },
				{ { -h, -h, -h }, { 0, 0, -1 }, { 1, 0 } },
				{ { -h, +h, -h }, { 0, 0, -1 }, { 1, 1 } },
				{ { +h, +h, -h }, { 0, 0, -1 }, { 0, 1 } },
					// left
				{ { -h, -h, -h }, { -1, 0, 0 }, { 0, 0 } },
				{ { -h, -h, +h }, { -1, 0, 0 }, { 1, 0 } },
				{ { -h, +h, +h }, { -1, 0, 0 }, { 1, 1 } },
				{ { -h, +h, -h }, { -1, 0, 0 }, { 0, 1 } },
					// top
				{ { -h, +h, +h }, { 0, 1, 0 }, { 0, 0 } },
				{ { +h, +h, +h }, { 0, 1, 0 }, { 1, 0 } },
				{ { +h, +h, -h }, { 0, 1, 0 }, { 1, 1 } },
				{ { -h, +h, -h }, { 0, 1, 0 }, { 0, 1 } },
					// bottom
				{ { -h, -h, -h }, { 0, -1, 0 }, { 0, 0 } },
				{ { +h, -h, -h }, { 0, -1, 0 }, { 1, 0 } },
				{ { +h, -h, +h }, { 0, -1, 0 }, { 1, 1 } },
				{ { -h, -h, +h }, { 0, -1, 0 }, { 0, 1 } },
				};

				GLuint const indices[3 * 2 * 6] = {
					0, 1, 2,
					2, 3, 0,

					4, 5, 6,
					6, 7, 4,

					8, 9, 10,
					10, 11, 8,

					12, 13, 14,
					14, 15, 12,

					16, 17, 18,
					18, 19, 16,

					20, 21, 22,
					22, 23, 20,
				};

				return be::gl::makeBasicMesh(vertices, indices);
			}
		}
	}
}
